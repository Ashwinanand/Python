"""Loops:
A repetition structure causes a statement or set of statements to execute multiple times until this repetition is stopped using a special value.

With the while loop we can execute a set of statements as long as a condition is true."""
# Loops
# Print numbers from 1 to 10 using while loop

number = 0
print("Numbers from 1-10 using while loop :")
while(number<10):
    #increment number by one every time condition is satisfied.
    number = number +1
    print(number)
